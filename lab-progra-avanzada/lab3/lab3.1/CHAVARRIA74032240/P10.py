set1 = {2, 3, 7, 11}

print(4 in set1)                       # False
print(3 in set1)                       # True
print(len(set1))                       # 4
print(max(set1))                       # 11
print(min(set1))                       # 2
print(sum(set1))                       # 23
print(set1.issubset({2, 3, 6, 7, 11})) # True
print(set1.issuperset({2, 3, 7, 11}))  # True
