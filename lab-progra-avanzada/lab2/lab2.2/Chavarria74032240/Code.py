# Creamos la tupla
t = (1, 2, 3, 7, 9, 0, 5)

print(t)         # Muestra toda la tupla
print(t[0])      # Imprime el primer valor
print(t[1: 3])   # Imprime desde la posición 1 hasta antes de la 3
print(t[-1])     # Imprime el último valor
print(t[: -1])   # Imprime todo menos el último
print(t[1 : -1]) # Imprime desde el segundo hasta el penúltimo
